package edu.montana.gsoc.msusel.quamoco.model.qm;

import com.thoughtworks.xstream.annotations.XStreamAlias;

/**
 * Tag -
 *
 * @author Isaac Griffith
 */
@XStreamAlias("tags")
public class Tag extends AbstractQMEntity {

    /**
     *
     */
    public Tag(final String name, final String description, final String id)
    {
        if (name == null || name.isEmpty() || id == null || id.isEmpty())
        {
            throw new IllegalArgumentException();
        }

        this.name = name;
        this.description = description;
        this.id = id;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object obj)
    {
        if (this == obj)
        {
            return true;
        }
        if (obj == null)
        {
            return false;
        }
        if (getClass() != obj.getClass())
        {
            return false;
        }
        final AbstractQMEntity other = (AbstractQMEntity) obj;
        if (description == null)
        {
            if (other.description != null)
            {
                return false;
            }
        }
        else if (!description.equals(other.description))
        {
            return false;
        }
        if (name == null)
        {
            if (other.name != null)
            {
                return false;
            }
        }
        else if (!name.equals(other.name))
        {
            return false;
        }
        return true;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public int hashCode()
    {
        final int prime = 31;
        int result = 1;
        result = prime * result + (description == null ? 0 : description.hashCode());
        result = prime * result + (name == null ? 0 : name.hashCode());
        return result;
    }

}
